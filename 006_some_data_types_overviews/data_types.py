# type()
# all data in python are objects

print(type(10))                                     # int
print(type(3.14))                                   # float
print(type("Hello Python"))                         # string
print(type('Hello'))                                # string
print(type([1, 2, 3, 4]))                           # list
print(type((1, 2, 3, 4, 5)))                        # tuple
print(type({'one': 1, 'two': 2, 'three': 3}))       # dictionary dict
print(type(2 == 4))                                 # boolean bool

